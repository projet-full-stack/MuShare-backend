    public function findAllActive($userId, $status): array
    {
        return $this->createQueryBuilder('f')
            ->andWhere('f.user = :userId')
            ->setParameter('userId', $userId)
            ->andWhere('f.status = :status')
            ->setParameter('status', $status)
            ->orderBy('f.id', 'ASC')
            ->getQuery()
            ->getResult();
